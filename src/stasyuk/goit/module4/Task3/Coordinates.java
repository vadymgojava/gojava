public class Coordinates {
    public static void main(String[] args) {
        double x1 = 2;
        double x2 = 3;
        double y1 = 2;
        double y2 = 3;
        double distance;
        distance = Math.sqrt((x2-x1)*(x2-x1)+(y2-y1)*(y2-y1));
        System.out.println(distance);

    }

}
